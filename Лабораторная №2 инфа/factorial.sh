#!/bin/bash

# Проверка наличия аргумента
if [ $# -ne 1 ]; then
    echo "Ошибка: Необходимо передать одно число в качестве аргумента"
    echo "Использование: $0 <число>"
    exit 1
fi

# Проверка, что аргумент - неотрицательное целое число
if ! [[ $1 =~ ^[0-9]+$ ]]; then
    echo "Ошибка: Аргумент должен быть неотрицательным целым числом"
    exit 1
fi

number=$1
factorial=1

# Вычисление факториала с использованием цикла for
if [ $number -eq 0 ]; then
    # Факториал 0 равен 1
    factorial=1
else
    for (( i=1; i<=$number; i++ )); do
        factorial=$((factorial * i))
    done
fi

# Вывод результата
echo "Факториал числа $number равен: $factorial"
